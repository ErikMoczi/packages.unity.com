namespace UnityEngine.XR.ARExtensions
{
    /// <summary>
    /// Represents the focus mode of the camera.
    /// </summary>
    public enum CameraFocusMode
    {
        /// <summary>
        /// Fixed focus mode. The focus does not change.
        /// </summary>
        Fixed,

        /// <summary>
        /// Auto focus mode. The focus will change automatically.
        /// </summary>
        Auto
    }
}
